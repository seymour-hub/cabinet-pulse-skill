def main():
    """Command-line interface for Pulse Engine."""
    if len(sys.argv) < 2:
        print("Usage: python pulse_engine.py <method> [json_params]")
        print("Methods: sunset_archive, sunrise_resurrection, system_status, purge_ready")
        sys.exit(1)
    
    method = sys.argv[1]
    params_str = sys.argv[2] if len(sys.argv) > 2 else "{}"
    
    try:
        params = json.loads(params_str)
    except json.JSONDecodeError:
        print(f"Error: Invalid JSON parameters: {params_str}")
        sys.exit(1)
    
    # Load default config
    config = {
        "storage": {"primary": "sqlite", "database_path": "./data/cabinet-pulse.db"},
        "schedule": {"archive_time": "02:00", "timezone": "Asia/Shanghai"},
        "kernel_layer": {"enabled": False}
    }
    
    engine = OpenSourcePulseEngine(config)
    
    try:
        if method == "sunset_archive":
            result = engine.execute_sunset_archive(params)
        elif method == "sunrise_resurrection":
            result = engine.execute_sunrise_resurrection(params)
        elif method == "system_status":
            result = engine.get_system_status(params)
        elif method == "purge_ready":
            result = engine.send_purge_ready_signal(params)
        else:
            print(f"Error: Unknown method: {method}")
            sys.exit(1)
        
        print(json.dumps(result, indent=2, ensure_ascii=False))
        
    except CabinetPulseError as e:
        print(json.dumps({"error": str(e), "status": "error"}, indent=2))
        sys.exit(1)
    except Exception as e:
        print(json.dumps({"error": f"Unexpected error: {str(e)}", "status": "error"}, indent=2))
        sys.exit(1)


if __name__ == "__main__":
    main()